    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtNo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Field ID History!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int reply = JOptionPane.showConfirmDialog(null,
                    "Anda Yakin Ingin Menghapus History " + txtNo.getText() + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    String sql = "DELETE FROM history WHERE id='" + txtNo.getText() + "'";
                    java.sql.Connection conn = (Connection) conf.configDB();
                    java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                    pstm.execute();
                    JOptionPane.showMessageDialog(null, "Data " + txtNo.getText() + " Telah Dihapus...", "Information", JOptionPane.INFORMATION_MESSAGE);
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
                showData();
                clear();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

