    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNo.getText().equals("") || txtId.getText().equals("") || txtUser.getText().equals("") || txtPc.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Field ID History!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int reply = JOptionPane.showConfirmDialog(null,
                    "Anda Yakin Ingin Mengedit History " + txtNo.getText() + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    String sql = "UPDATE history SET client_id=?, client_name=?, com_id=?  WHERE id = '" + txtNo.getText() + "'";
                    java.sql.Connection conn = (Connection) conf.configDB();
                    java.sql.PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setInt(1, Integer.parseInt(txtId.getText()));
                    ps.setString(2, txtUser.getText());
                    ps.setInt(3, Integer.parseInt(txtPc.getText()));
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "Data " + txtNo.getText() + " Sudah Terupdate...");
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
                showData();
                clear();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

